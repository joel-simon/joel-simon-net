<p>
  The creative motivation is to explore whether the internet - and the LLMs
  trained on it - offer new opportunities for creative collaborations, inverting
  the usual dynamic of internet taking our data to train models to one where it
  proactively can give back to us. The technical motivation is to investigate if
  open-ended evolutionary algorithms can complement LLMs and produce genuinely
  surprising novelty. The motivation is not to determine whether 'AI' can
  automate human creativity - a question that is deemed inhumane - but rather if
  it can inspire humans with possible directions for them to continue and fuel
  their own creativity, and potentially even foster new creative relationships.
</p>
