  <h2>Insights</h2>

  <!-- <h2>Evolutionary Dynamics</h2> -->
  <p>
    <strong>Creative strategies boost diversity:</strong> Formalized creative thinking
    strategies significantly increased novelty metrics compared to baseline outputs.
  </p>
  <p>
    <strong>Variation beats creation:</strong> Modifying existing artifacts produced
    more diverse results than generating new ones from scratch.
  </p>

  <!-- <h2>Strategy Performance</h2> -->
  <p>
    <strong>Domain-specific effectiveness:</strong> Replacement Template excelled
    for shaders while Conceptual Blending performed better for websites.
  </p>
  <p>
    <strong>Crossover amplifies novelty:</strong> Combining elements from multiple
    solutions yielded the highest novelty scores in the experiments indicating constraints
    can help counter the innate laziness.
  </p>

  <!-- <h2>Complexity Correlation</h2> -->
  <p>
    <strong>Novelty-complexity link:</strong> More complex artifacts (longer code)
    explored more novel territory in the latent space. The most diverse run using
    crossover and creative strategies produced outputs nearly twice as long on average
    and the runs without.
  </p>
  <!-- <p>
    <strong>Two-phase exploration:</strong> Rapid initial discovery followed by steady
    incremental growth, similar to human creative processes.
  </p> -->

  <!-- <h2>System Configuration</h2> -->
  <p>
    <strong>Reasoning has diminishing returns:</strong> Higher reasoning levels didn't
    significantly increase diversity, although it may improve quality.
  </p>
  <p>
    <strong>Context awareness matters:</strong> Population summaries consistently
    improved performance, highlighting the importance of evolutionary context.
  </p>

  <!-- <h2>AI Creativity Implications</h2> -->
  <p>
    <strong>LLMs default to average:</strong> Without evolutionary pressure, language
    models produce homogeneous solutions rather than exploring creative boundaries.
  </p>
  <p>
    <strong>General applicability:</strong> Similar novelty dynamics persisted across
    all tested domains (shaders, clocks, architecture), remaining unsaturated after
    60 generations.
  </p>
