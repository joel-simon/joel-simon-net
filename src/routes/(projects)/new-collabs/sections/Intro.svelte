<p class="mb-2">
  <b>New Collabs</b> is a research project combining open-ended evolutionary (OEE)
  algorithms with large language models (LLMs) to generate candidate ideas for collaborations.
  It presents a novel algorithm for open-ended ideation using co-evolved chain-of-thought
  (COT) templates. The algorithm is tested with creative thinking COT's applied to
  scraped bios of my friends mixed with some well known artists.
</p>
